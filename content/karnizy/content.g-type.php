	<h1>Г-образные карнизы для ванной</h1>
	<p>
		Изготавливаем Г-образные карнизы из нержавеющей стали для ванн разных размеров и с разными способами крепления. Стандартным вариантом является крепление к стенам с опциональной вертикальной штангой от потолка. Карнизы из нержавейки отличаются почти неограниченным сроком службы.
