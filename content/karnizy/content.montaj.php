	<h3 id="gips">Подвесной потолок ГИПСОКАРТОН, за ним основной бетонный</h3>
	<p>
		При монтаже разборка потолка не требуется. Для промежуточной шпильки держателя необходимо просверлить отверстие в гипсокартоне &Oslash; 14–16 мм, которое в дальнейшем закроется шайбой &Oslash; 38 мм. Через это отверстие перфораторным сверлом &Oslash; 12 мм надо просверлить в бетонном потолке отверстие на глубину 35–40 мм для забивки анкера М10х40 (длина перфораторных свёрл в продаже в магазинах от 15 см до 1 метра). Анкер можно забивать проточенным концом шпильки предварительно, с другой стороны закрутив болт М6х10 (чтобы не забить резьбу). Шпилька к карнизу даётся с запасом для обрезки проточенного конца после забивки.
