	<h3 id="natyajnoy">Натяжной подвесной потолок</h3>
	<p>
		Более 30% наших заказчиков имеют в ванных комнатах натяжные потолки. Технология установки держателя схожа с монтажом через вышеуказанные подвесные потолки (реечный, гипсокартонный). Особенность натяжного потолка – в приклеивание пластикового армирующего кольца (даётся с карнизом).
	</p>
	<p><b>Определите на потолке точку где будет расположен держатель.</b></p>
	<p><b>Приложите кольцо и обведите отверстие. Это поможет в дальнейшем точно приклеить кольцо.</b></p>

	<p class="width50" style="width:100%;"><a class="fancybox" rel="natyajnoy" href="/upload/montaj/montaj_natyajnoy_01.jpg"><img src="/upload/montaj/montaj_natyajnoy_01.jpg" alt=""></a></p>

	<p>
		<b>Обезжирьте кольцо в месте нанесения клея и потолок где будет приклеиваться кольцо.</b>
	</p>
	<p>
		<b>Нанесите клей на кольцо.</b>
	</p>

	<p class="width50" style="width:100%;"><a class="fancybox" rel="natyajnoy" href="/upload/montaj/montaj_natyajnoy_02.jpg"><img src="/upload/montaj/montaj_natyajnoy_02.jpg" alt=""></a></p>

	<p>
		<b>Прижмите с небольшим усилием кольцо к натяжному потолку.</b>
