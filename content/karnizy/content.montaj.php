	<h2 id="sovety">Полезные советы <span>(от Фирмы «SNB»)</span></h2>

	<p>
		<ol>
			<li>
				Во избежание попадания воды на пол <b>опускайте шторку в ванну на 15-17см</b>.
				При этом размере шторка не находится постоянно в воде и не выпадает из ванны от струи душа.
			</li>
			<li>
				Шторки для ванн выпускаются высотой <b>только 180см или 200см</b>.
				До монтажа карниза приобретите шторку, она будет определять расстояние
				карниза от ванны. При покупке шторы, проверяйте размер отверстий(люверсы) для
				крепления шторки. Шторки большинства фирм имеют отв. 7.5-8мм под наши ролики.
			</li>
			<li>
				К каждому карнизу прилагается «Инструкция для монтажа». Внимательно изучите её и Вы установите карниз не хуже профессионалов.
			</li>
			<li>
				Для <a title="Г-образные карнизы" href="g-type">Г-обр. карнизов</a> держатель устанавливается по длинной стороне, ближе к радиусу изгиба.
			</li>
			<li>
				Подрезайте шпильку в нужный размер со стороны <b>где нет на торце отверстия М6</b>.
			</li>
			<li>
				При сверлении отверстий под пластиковые дюбеля, желательно пользоваться специальными перьевыми свёрлами диаметром 8 мм для керамической плитки и стекла.
			</li>
			<li>
				На упаковке для шторки указаны режимы стирки и глаженья, запомните их.
			</li>
			<li>
				Карниз изготовлен из полированной нержавеющей стали AISI-304, производство ИТАЛИЯ. Пользуйтесь средствами по уходу за нержавеющей сталью.
			</li>
			<li>
				Ролики вставлять в паз карниза, разрезанным конусом наружу!
			</li>
		</ol>
