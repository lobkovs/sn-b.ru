	<h1>Выбор карниза для ванн</h1>
	<p>
		Установка карниза – немаловажная часть обустройства интерьера ванной комнаты. При выборе карниза необходимо учитывать эстетические качества, прочность и долговечность. Для <a href="g-type">Г-образных</a>, <a title="Радиусные карнизы" href="radiusnie">радиусных</a>, <a title="Овальные карнизы" href="ovalnie">овальных</a> и <a title="Круглые карнизы" href="kruglie">круглых</a> карнизов во избежание прогиба и провисания необходимо дополнительное потолочное крепление – ДЕРЖАТЕЛЬ (см. «<a title="Конструкция карниза" href="konstruktsiya">Конструкция карниза</a>»), выдерживающий нагрузку не менее 20кг. Выбрав наш карниз, Вы можете быть уверены, что он не потеряет своих эстетических и прочностных параметров в течение всего срока службы ванны 15 – 20 лет. Если Вы уже сделали ошибку в выборе карниза, мы поможем её исправить, установив наш карниз. Отверстия в стенах от крепления прежнего карниза эстетично прикроются полированными пластинами.
	</p>
