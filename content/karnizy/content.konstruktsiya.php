	<p>
		Ролики изготовлены из прочного и водостойкого пластика с неограниченным сроком службы. При нажатии люверса, отверстие которого 8мм, лепестки разрезанного конуса (&Oslash; 9,5мм) сэимаются до 8мм пропуская люверс, после чего разжимаются и прочно держат шторку на роликах.
